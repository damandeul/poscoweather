export function pickTrivia(w: SiteWeather): string {
  const pool = RULES.filter((r) => r.match(w)).flatMap((r) => r.texts);
  // 시간대별로 로테이션 — 같은 시간에는 같은 문장 유지
  const hour = Math.floor(w.fetchedAt / 3_600_000);
  return pool[hour % pool.length];
}
